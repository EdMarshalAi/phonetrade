async function main() {
  loadEnv();
  const db = createClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.SUPABASE_SERVICE_ROLE_KEY!,
    { auth: { persistSession: false } }
  );

  const updates = [
    {
      slug: "delivery",
      content: DELIVERY,
      meta_title: "Доставка и самовывоз — PhoneTrade Белгород",
      meta_description:
        "Доставка техники Apple по Белгороду — бесплатно, самовывоз из шоурума на ул. Попова, 36. Доставка по России курьерскими службами EMS и СДЭК от 500 ₽.",
    },
    {
      slug: "warranty",
      content: WARRANTY,
      meta_title: "Гарантия, обмен и возврат — PhoneTrade Белгород",
      meta_description:
        "Официальная гарантия на оригинальную технику Apple в Белгороде: 1 год на технику Apple, условия обмена и возврата товара по Закону о защите прав потребителей.",
    },
  ];

  for (const u of updates) {
    const { error } = await db
      .from("static_pages")
      .update({
        content: u.content,
        meta_title: u.meta_title,
        meta_description: u.meta_description,
        updated_at: new Date().toISOString(),
      })
      .eq("slug", u.slug);
    console.log(error ? `✗ ${u.slug}: ${error.message}` : `✓ ${u.slug} — ${u.content.length} символов`);
  }
}

main().then(() => process.exit(0));
