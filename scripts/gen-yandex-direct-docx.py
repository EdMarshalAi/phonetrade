st.font.color.rgb = INK

def h1(t):
    p = doc.add_paragraph()
    r = p.add_run(t); r.bold = True; r.font.size = Pt(18); r.font.color.rgb = INK
    p.space_after = Pt(6); return p

def h2(t):
    p = doc.add_paragraph()
    r = p.add_run(t); r.bold = True; r.font.size = Pt(13.5); r.font.color.rgb = INK
    p.paragraph_format.space_before = Pt(12); p.paragraph_format.space_after = Pt(4); return p

def h3(t):
    p = doc.add_paragraph()
    r = p.add_run(t); r.bold = True; r.font.size = Pt(11.5); r.font.color.rgb = ACC
    p.paragraph_format.space_before = Pt(8); p.paragraph_format.space_after = Pt(2); return p

def para(t, color=INK, size=10.5, bold=False, italic=False):
    p = doc.add_paragraph()
    r = p.add_run(t); r.font.color.rgb = color; r.font.size = Pt(size); r.bold = bold; r.italic = italic
    return p

def bullet(t, color=INK):
    p = doc.add_paragraph(style='List Bullet')
    r = p.add_run(t); r.font.color.rgb = color; r.font.size = Pt(10.5)
    return p

def kv_table(rows, widths=(5.5, 11.0)):
    tbl = doc.add_table(rows=0, cols=2)
    tbl.style = 'Light Grid Accent 1'
    tbl.alignment = WD_TABLE_ALIGNMENT.LEFT
    for k, v in rows:
        c = tbl.add_row().cells
        rk = c[0].paragraphs[0].add_run(k); rk.bold = True; rk.font.size = Pt(10)
        rv = c[1].paragraphs[0].add_run(v); rv.font.size = Pt(10)
    for row in tbl.rows:
        row.cells[0].width = Cm(widths[0]); row.cells[1].width = Cm(widths[1])
    return tbl

def ad_block(title1, title2, text, note=None):
    """Карточка объявления РСЯ."""
    tbl = doc.add_table(rows=0, cols=2); tbl.style = 'Light List Accent 1'
    def add(label, value, vcolor=INK, count=None):
        c = tbl.add_row().cells
        rl = c[0].paragraphs[0].add_run(label); rl.bold = True; rl.font.size = Pt(9.5); rl.font.color.rgb = MUT
        pv = c[1].paragraphs[0]
        rv = pv.add_run(value); rv.font.size = Pt(10.5); rv.font.color.rgb = vcolor
        if count is not None:
            rc = pv.add_run(f"   ({len(value)} симв.)"); rc.font.size = Pt(8.5); rc.font.color.rgb = MUT
        c[0].width = Cm(3.8); c[1].width = Cm(12.7)
    add("Заголовок 1", title1, count=True)
    add("Заголовок 2", title2, count=True)
    add("Текст", text, count=True)
    if note:
        add("Куда ведёт", note, vcolor=ACC)
    doc.add_paragraph()

